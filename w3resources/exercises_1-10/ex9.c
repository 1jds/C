#include <stdio.h>

int main() {
  int first_int, second_int, sum;
  printf("Input the first integer: ");
  scanf("%d", &first_int);
  printf("Input the second integer: ");
  scanf("%d", &second_int);
  sum = first_int + second_int;
  printf("Sum of the above two integers = %d\n", sum);
  return 0;
}



